k= float( input("enter value in Km to convert into Miles")) #here float is used because input may be in float and also result may be in float
m= k/ 1.609344
print("The conversion value in Miles is=",round(m,4))
#on last line ROUND function is used to round off the final result to a particular decimal place, 
#in bracket 4 is no of decimal places to round off the number.